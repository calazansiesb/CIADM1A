@st.cache_data # Armazena em cache o carregamento dos dados para melhor performance
def load_data(url):
    try:
        # Tenta com latin1 primeiro, que √© comum para CSVs brasileiros
        df = pd.read_csv(url, sep=";", encoding="latin1")
    except UnicodeDecodeError:
        # Se latin1 falhar, tenta com utf-8
        df = pd.read_csv(url, sep=";", encoding="utf-8")
    
    # Tentativa de converter colunas que deveriam ser num√©ricas
    # para num√©ricas, tratando poss√≠veis v√≠rgulas como decimais e NaNs
    for col in df.columns:
        if df[col].dtype == 'object': # Se a coluna √© de texto (object)
            try:
                # Substituir v√≠rgula por ponto para convers√£o para float
                df[col] = df[col].astype(str).str.replace(',', '.', regex=False)
                df[col] = pd.to_numeric(df[col], errors='coerce') # 'coerce' transforma erros em NaN
            except ValueError:
                pass # N√£o faz nada se n√£o conseguir converter, mant√©m como texto
    
    return df

